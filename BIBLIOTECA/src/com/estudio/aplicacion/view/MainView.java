    private void txBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txBuscarActionPerformed
        // TODO add your handling code here:
        String textoBuscado = txBuscar.getText();
        Ejemplar ejemBuscado = buscarEjemplar(textoBuscado);
        if (ejemBuscado != null) {
            JOptionPane.showMessageDialog(this, "Ejemplar encontrado: " + ejemBuscado);
        } else {
            JOptionPane.showMessageDialog(this, "Ejemplar no encontrado.");
        }

        List<Ejemplar> listaBuscada = buscarEjemplares(textoBuscado);
        if (!listaBuscada.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ejemplares encontrados: " + listaBuscada);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontraron ejemplares.");
        }

        
    }//GEN-LAST:event_txBuscarActionPerformed
    public Ejemplar buscarEjemplar(String nombre) {
        for (Ejemplar ejem : ejemplares) {
            if (ejem.getNombre().equals(nombre)) {
                return ejem;
            }
        }
        return null;  // Si no se encuentra el ejemplar
    }

    public List<Ejemplar> buscarEjemplares(String nombre) {
        List<Ejemplar> encontrados = new ArrayList<>();
        for (Ejemplar ejem : ejemplares) {
            if (ejem.getNombre().equals(nombre)) {  // Utiliza contains para b√∫squeda parcial
                encontrados.add(ejem);
            }
        }
        return encontrados;
    }
    
    
    
    
    
    
    
